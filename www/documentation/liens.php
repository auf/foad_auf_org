<p>Les liens non visités sont bleus, les liens visités rouges.</p>

<p>Si vous souhaitez passer à nouveau en revue toutes les candidatures,
il peut être utile de faire en sorte que votre navigateur considère
tous les liens comme non visités.</p>

<p>C'est très facile avec Firefox et son extension
<i><span xml:lang="en" class="en">web developer</span></i>.</p>


<h3>Installation</h3>

<ol>
<li>Utilisez Firefox pour vous rendre sur le site de l'extension
    <i><span xml:lang="en" class="en">web developer</span></i>&nbsp;:
	<a target="_blank" 
	href="http://joliclic.free.fr/mozilla/webdeveloper/"
	>version française</a> ou 
	<a hreflang="en" target="_blank"
	href="http://www.chrispederick.com/work/firefox/webdeveloper/"
	>originale en anglais</a>.
	</li>
<li>Cliquez sur le lien proposé pour l'installation. Vous devrez éventuellement
    d'abord autoriser l'installation à partir du site choisi.
<li>Fermez (toutes les fenêtres de) Firefox et relancez le pour terminer
	l'installation.</li>
</ol>

<h3>Utilisation</h3>

<p>La barre d'outils de l'extension est facilement activable ou désactivable
par un simple clic droit (CTRL + clic sur Mac) sur les barres d'outils.</p>
